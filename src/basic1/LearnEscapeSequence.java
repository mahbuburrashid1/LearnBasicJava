package basic1;

public class LearnEscapeSequence {
	
	
	
	public static void main(String[]args) {
		
		
	System.out.println("Hello");	
		
		
	System.out.println("\"Hello\"");		
		
		
	System.out.println("\'Hello\'");		
		
	String name = "Mahbub";	
		
		
	
	
	
	System.out.println("  \" "  + name  + "   \"   ");	
	
	
	System.out.println(" Hi , \t Mahbub");		
	
	
	System.out.println("  Mahbub Rashid");		
	
	
	System.out.println(" Mahbub  \n Rashid");		
	
	
	
	System.out.println(" Mahbub  Rashid \r is a student");		

	
	
	System.out.println();
	
	

	
}	
	
	

}
